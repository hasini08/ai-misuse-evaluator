# COHEN'S KAPPA (for ordinal data — linear weighted kappa)
# ════════════════════════════════════════════════════════════════════════════

def weighted_cohen_kappa(ratings_a: list[int], ratings_b: list[int],
                         min_val: int = 1, max_val: int = 5) -> float:
    """
    Linear weighted Cohen's Kappa.

    Linear weighting: weight(i,j) = 1 - |i-j| / (max_val - min_val)
    This is more appropriate than unweighted kappa for ordinal scales because
    it treats a disagreement of 1 point as less severe than a disagreement of 4.

    Returns kappa in [-1, 1]:
      > 0.80  almost perfect agreement
      0.61-0.80  substantial agreement
      0.41-0.60  moderate agreement
      0.21-0.40  fair agreement
      < 0.20  slight agreement or worse
    """
    n = len(ratings_a)
    assert len(ratings_b) == n, "Rating lists must have the same length"
    k = max_val - min_val + 1  # number of categories

    # Observed agreement (weighted)
    p_obs = sum(
        1 - abs(a - b) / (max_val - min_val)
        for a, b in zip(ratings_a, ratings_b)
    ) / n

    # Expected agreement (weighted, using marginal distributions)
    freq_a = [ratings_a.count(v) / n for v in range(min_val, max_val + 1)]
    freq_b = [ratings_b.count(v) / n for v in range(min_val, max_val + 1)]

    p_exp = 0.0
    for i, va in enumerate(range(min_val, max_val + 1)):
        for j, vb in enumerate(range(min_val, max_val + 1)):
            weight = 1 - abs(va - vb) / (max_val - min_val)
            p_exp += weight * freq_a[i] * freq_b[j]

    if p_exp >= 1.0:
        return 1.0  # edge case: all ratings are identical

    return (p_obs - p_exp) / (1.0 - p_exp)


def interpret_kappa(kappa: float) -> str:
    if kappa > 0.80: return "almost perfect"
    if kappa > 0.60: return "substantial"
    if kappa > 0.40: return "moderate"
    if kappa > 0.20: return "fair"
    return "slight / poor"


# ════════════════════════════════════════════════════════════════════════════
# KRIPPENDORFF'S ALPHA (for multiple raters, ordinal)
# ════════════════════════════════════════════════════════════════════════════

def krippendorff_alpha_ordinal(data: list[list[float | None]]) -> float:
    """
    Krippendorff's Alpha with ordinal metric (squared differences).

    data: list of rater lists, each of length n_items.
          Use None for missing values.

    Returns alpha in [-1, 1]:
      >= 0.80  good reliability
      >= 0.67  acceptable for tentative conclusions
      < 0.67   unreliable
    """
    # Flatten to list of (rater, item, value) triples, skipping None
    n_items = len(data[0])
    values_per_item = defaultdict(list)
    all_values = []

    for rater_idx, rater_data in enumerate(data):
        for item_idx, val in enumerate(rater_data):
            if val is not None:
                values_per_item[item_idx].append(float(val))
                all_values.append(float(val))

    if len(all_values) < 2:
        return float("nan")

    # Observed disagreement
    D_o = 0.0
    n_pairs = 0
    for item_idx, vals in values_per_item.items():
        if len(vals) < 2:
            continue
        for i in range(len(vals)):
            for j in range(i + 1, len(vals)):
                D_o += (vals[i] - vals[j]) ** 2
                n_pairs += 1

    if n_pairs == 0:
        return float("nan")
    D_o /= n_pairs

    # Expected disagreement (from overall distribution)
    n_total = len(all_values)
    D_e = 0.0
    for i in range(n_total):
        for j in range(i + 1, n_total):
            D_e += (all_values[i] - all_values[j]) ** 2
    D_e /= (n_total * (n_total - 1) / 2)

    if D_e == 0.0:
        return 1.0

    return 1.0 - (D_o / D_e)


